package com.o2o_jiangchen.model;

public class Grid0model {
	private int img;
	private String name;
	public int getImg() {
		return img;
	}
	public void setImg(int img) {
		this.img = img;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	@Override
	public String toString() {
		return "Grid0model [img=" + img + ", name=" + name + "]";
	}
	
}
